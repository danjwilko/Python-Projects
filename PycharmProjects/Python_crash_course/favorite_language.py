# A dictionary of similar objects

favorite_languages = {
    'jen': 'python',
    'sarah': 'c',
    'edward': 'ruby',
    'phil': 'python',
}
# We've broken the dictionary down into several lines.
# We also have a comma after the last key pair just in case we want to add another rvalue It's ready to go.

language = favorite_languages['sarah'].title()
print(f"Sarah's favorite language is {language}")

